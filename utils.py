def data_sampling(text, label, property_pair, prompt_sentence, case, sampling_num=4) : 
    text_n = []
    prompt_sentence_n = []
    one_hot_label_n = []
    if case == "entity" : 
        for text_i in text :       
            for _ in range(sampling_num) :
                text_n.append(text_i)
        for label_name in label : 
            for true_i in range(int(sampling_num/2)) :
                prompt_sentence_n.append(prompt_sentence.format(label_name))     # true label
                one_hot_label_n.append([1., 0.])                                 # true label
            property_pair_copy = copy.deepcopy(property_pair)
            property_pair_copy.remove(label_name)
            false_label_idx = random.randint(low=len(property_pair_copy), size=int(sampling_num/2)) 
            for false_idx in false_label_idx :
                false_property_name = property_pair_copy[false_idx]
                prompt_sentence_n.append(prompt_sentence.format(false_property_name))   # false label
                one_hot_label_n.append([0., 1.])                                         # false label
        
        R(4).shuffle(text_n)                    # random shuffle
        R(4).shuffle(prompt_sentence_n)
        R(4).shuffle(one_hot_label_n)
        return text_n, prompt_sentence_n, one_hot_label_n

    else : 
        return text, label, []

def make_prompt(label, property_pair, prompt_sentence, case) : 
    label_prompt = []
    for label_i in label :
        label_prompt.append(prompt_sentence.format(label_i)) 
    return label_prompt
